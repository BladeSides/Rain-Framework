using System.Collections;
using System.Collections.Generic;
using UnityEngine;

namespace RainFramework.Structures
{
    // Class for holding structures used throughout RainFramework
    // Written for RainFramework by BladeSides

    // Bool Vectors: Vectors holding only bools
    [System.Serializable]
    public struct Vector2Bool
    {
        public bool X;
        public bool Y;
    }
    [System.Serializable]
    public struct Vector3Bool
    {
        public bool X;
        public bool Y;
        public bool Z;
    }

    // String Vector, used for holding key:value pairs
    [System.Serializable]
    public struct Vector2String
    {
        public string Key;
        public string Value;
    }

}
